void main() {
  int valor1 = 4;
  int valor2 = 7;

  if (valor1 < valor2) {
    print("$valor1 < $valor2");
  } else if (valor1 > valor2) {
    print("$valor1 > $valor2");
  } else if (valor1 >= valor2) {
    print("$valor1 >= $valor2");
  } else if (valor1 <= valor2) {
    print("$valor1 <= $valor2");
  } else if (valor1 == valor2) {
    print("$valor1 == $valor2");
  } else if (valor1 != valor2) {
    print("$valor1 != $valor2");
  }
}
